package exam;

public class Example13 {
	public static void main(String[] args) {
		/*
		 * 논리 부정 연산자
		 * - boolean 값을 가지는 피연산자 앞에 (!)를 붙여서 값을 반대로 바꾸는 역할
		 * 
		 * 즉) true => false,
		 * 	  false => true
		 * 프로그래밍에서 !는 부정을 뜻함
		 */
		boolean isHuman = true;
		System.out.println(isHuman);
		System.out.println(!isHuman);
	}
}
